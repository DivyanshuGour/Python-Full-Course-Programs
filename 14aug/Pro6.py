wd = int(input("WeekDay : ")) # 12

if wd<1 or wd>7:
	print("\tWrong WD")
elif wd==1:
	print("\tMonday")
elif wd==2:
	print("\tTuesday")
elif wd==3:
	print("\tWednesday")
elif wd==4:
	print("\tThursday")
elif wd==5:					
	print("\tFriday")
elif wd==6:
	print("\tSaturday")
else:							
	print("\tSunday")						
	
	
	

